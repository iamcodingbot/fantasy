class Player{
    constructor(playerId, gameId, firstname, lastname, playerPropList, teamname, color, cost){
        this.playerId = playerId;
        this.gameId = gameId;
        this.firstname = firstname;
        this.lastname = lastname;
        this.playerPropList = playerPropList;
        this.teamname = teamname;
        this.color = color;
        this.cost = cost;
    }
}

export default Player; 